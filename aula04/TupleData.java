package aula04;

public class TupleData {
    private int numermec;
    private int idbook;
    
    public TupleData (int numermec,int idbook){
        this.numermec = numermec;
        this.idbook = idbook;
    }
    
    public int getIdbook() {
        return idbook;
    }
    public int getNumermec() {
        return numermec;
    }
}
